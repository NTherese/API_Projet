         int mois=Integer.parseInt(date.substring(4,5));
         int an=Integer.parseInt(date.substring(6));
            System.out.println(date);
            System.out.println(an+" "+mois+" "+jour);
        LocalDate datepres=LocalDate.of(an, mois,jour);
        
